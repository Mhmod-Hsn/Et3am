import UIKit

extension UIColor {
    
    class func primaryEt3am() -> UIColor {
        return UIColor(hexString: "2C3E50")
    }
    
    class func secondaryEt3am() -> UIColor {
        return UIColor(hexString: "E74C3C")
    }
    
    class func greyEt3am() -> UIColor {
        return UIColor(hexString: "ECF0F1")
    }
    
    class func lightBlueEt3am() -> UIColor {
        return UIColor(hexString: "3498DB")
    }
    
    class func darkBlueEt3am() -> UIColor {
        return UIColor(hexString: "2980B9")
    }
    
    class func darkGreenEt3am() -> UIColor {
        return UIColor(hexString: "099C4F")
    }
    
    class func lightGreenEt3am() -> UIColor {
        return UIColor(hexString: "3CE88E")
    }
}
